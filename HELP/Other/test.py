current_height = 0  # текущата височина на мравката
day = 0  # брояч за деня

while current_height <= 20:

    current_height += 2  # мравката се изкачва с 2 метра през деня

    if current_height >= 20:
        break  # ако мравката е достигнала или надвишила 20 метра, прекратяваме цикъла

    current_height -= 1  # мравката слиза с 1 метър през нощта
    day += 1  # увеличаваме броя на деня
print(f"Мравката ще е на 20-тия метър на {day}-тия ден.")