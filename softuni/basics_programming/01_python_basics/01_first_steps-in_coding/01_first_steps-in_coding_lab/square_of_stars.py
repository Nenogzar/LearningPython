
n = int(input("Въведете цяло положително число: "))  # Въвеждане на числото N от потребителя
s = input("Въведете символ: ")      # Въвеждане на символа s от потребителя

for i in range(n):
    if i == 0 or i == n-1:
        print(s * n)  # Отпечатване на цялата линия от символи за първия и последния ред
    else:                      # Отпечатване на първия и последната символа, заедно
        print(s + " "*(n-2) + s)  # с празните пространства между тях

